--DROP TABLE TRUOC KHI TAO TABLE, PRIMARY KEY
USE QLBANHANG

DROP TABLE IF EXISTS CUNGCAP
CREATE TABLE CUNGCAP(
	CungCapID INT NOT NULL
	, CONSTRAINT PK_CUNGCAPID PRIMARY KEY(CungCapID), --Dat ten cho CONSTRAINT PRIMARY KEY
	TenDayDu NVARCHAR(50) NOT NULL,
	TenLienHe NVARCHAR(50),
	DiaChi NVARCHAR(50),
	ThanhPho NVARCHAR(50),
	MaBuuDien NVARCHAR(10),
	QuocGia NVARCHAR(30),
	DienThoai NVARCHAR(50)
)
GO

DROP TABLE IF EXISTS DANHMUC
CREATE TABLE DANHMUC(
	DanhMucID INT NOT NULL
	, CONSTRAINT PK_DANHMUCID PRIMARY KEY (DanhMucID),
	TenDanhMuc NVARCHAR(30),
	MoTa NVARCHAR(50)
)
GO

DROP TABLE IF EXISTS SANPHAM
CREATE TABLE SANPHAM(
	SanPhamID INT NOT NULL
	, CONSTRAINT PK_SANPHAMID PRIMARY KEY(SanPhamID),
	TenSanPham NVARCHAR(50),
	CungCapID INT,
	DanhMucID INT,
	DonVi NVARCHAR(10),
	Gia float
)
GO

DROP TABLE IF EXISTS DONHANGCHITIET
CREATE TABLE DONHANGCHITIET(
	DonHangChiTietID INT NOT NULL
	, CONSTRAINT PK_DONHANGCHITIET PRIMARY KEY(DonHangChiTietID),
	DonHangID INT,
	SanPhamID INT,
	SoLuong INT
)
GO

DROP TABLE IF EXISTS DONHANG
CREATE TABLE DONHANG(
	DonHangID INT NOT NULL
	, CONSTRAINT PK_DONHANGID PRIMARY KEY(DonHangID),
	KhachHangID INT,
	NhanVienID INT,
	NgayDatHang DATE,
	ShipperID INT
)
GO

DROP TABLE IF EXISTS KHACHHANG
CREATE TABLE KHACHHANG(
	 KhachHangID INT NOT NULL
	 , CONSTRAINT PK_KHACHHANGID PRIMARY KEY(KhachHangID),
	 HoTen NVARCHAR(50),
	 TenLienLac NVARCHAR(30),
	 DiaChi NVARCHAR(100),
	 ThanhPho NVARCHAR(40),
	 MaBuuDien CHAR(3),
	 QuocTich NVARCHAR(30)
)
GO

DROP TABLE IF EXISTS NHANVIEN
CREATE TABLE NHANVIEN(
	NhanVienID INT NOT NULL
	, CONSTRAINT PK_NHANVIENID PRIMARY KEY(NhanVienID),
	Ten NVARCHAR(20),
	Ho NVARCHAR(50),
	NgaySinh DATE,
	Anh CHAR(30),
	GhiChu NVARCHAR(100)
)
GO

DROP TABLE IF EXISTS SHIPPERS
CREATE TABLE SHIPPERS(
	ShipperID INT NOT NULL
	, CONSTRAINT PK_SHIPPERID PRIMARY KEY(ShipperID),
	HoTen NVARCHAR(50),
	SoDienThoai CHAR(15)
)
GO

--TAO FOREIGN KEY
ALTER TABLE SANPHAM ADD
	CONSTRAINT FK_CUNGCAPID FOREIGN KEY (CungCapID) REFERENCES CUNGCAP(CungCapID),
	CONSTRAINT FK_DANHMUCID FOREIGN KEY (DanhMucID) REFERENCES DANHMUC (DanhMucID)

ALTER TABLE DONHANGCHITIET ADD
	CONSTRAINT FK_SANPHAMID FOREIGN KEY (SanPhamID) REFERENCES SANPHAM(SanPhamID),
	CONSTRAINT FK_DONHANGID FOREIGN KEY (DonHangID) REFERENCES DONHANG(DonHangID)

ALTER TABLE DONHANG ADD
	CONSTRAINT FK_KHACHHANGID FOREIGN KEY (KhachHangID) REFERENCES KHACHHANG(KhachHangID),
	CONSTRAINT FK_NHANVIENID FOREIGN KEY (NhanVienID) REFERENCES NHANVIEN(NhanVienID),
	CONSTRAINT FK_SHIPPERID FOREIGN KEY (ShipperID) REFERENCES SHIPPERS(ShipperID)
GO

--Tao rang buoc CHECK
ALTER TABLE SANPHAM ADD
	CONSTRAINT CK_GIA CHECK (Gia>0),
	CONSTRAINT CK_DONVI CHECK (DonVi IN(N'thùng', N'chai', N'túi', N'hộp', N'con', N'lốc', N'kg', N'gói', N'cân'))

ALTER TABLE NHANVIEN ADD CONSTRAINT CK_NGAYSINH CHECK (NgaySinh<getDATE())

ALTER TABLE DONHANG ADD CONSTRAINT CK_NGAYDATHANG CHECK (NgayDatHang>=getDATE())

ALTER TABLE DONHANGCHITIET ADD CONSTRAINT CK_SOLUONG CHECK (SoLuong>0)
GO

